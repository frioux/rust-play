use std::ops::Add;
fn add<T: Add + Add<Output = T>>(a: T, b: T) -> T {
    return a + b;
}

#[derive(Debug)]
struct Rectangle {
    width: u32,
    height: u32,
}

impl Rectangle {
    fn can_hold(&self, other: &Rectangle) -> bool {
        self.width > other.width && self.height > other.height
    }
}
